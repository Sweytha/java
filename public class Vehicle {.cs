public class Vehicle {
//instance variables go here
    private String Make;
    private int year;
    private String Model;

//creating the setter for Make
    public void setMake(String make) {
        this.make = make;
    }

//creating the getter for Make
    public String getMake() {
        return make;
    }
//creating the setter for Year
    public void setYear(int year) {
        this.year = year;
    }
//creating the getter for Year
    public int getYear() {
        return year;
    }
//creating the setter for Model
    public void setModel(String Model) {
        this.Model= Model;
    }
//creating the getter for Model
    public String getModel() {
        return Model;
    }


}